    private String getImagePath(){
        JFileChooser fc = new JFileChooser();
        fc.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image files", ImageIO.getReaderFileSuffixes());
        fc.setAcceptAllFileFilterUsed(false);
        fc.addChoosableFileFilter(filter);
        int result=fc.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File selectedFile = fc.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            return path;
        }
        return null;
    }
    
    private List isProfileValid(){
        List msg = new ArrayList();
        msg.add(Validation.validateName(txtName.getText()));
        msg.add(Validation.validateDOB(txtDateofBirth.getText()));
        msg.add(Validation.validateSsn(txtSsn.getText()));
        msg.add(Validation.validateBirthCN(txtBirthCertificate.getText()));
        msg.add(Validation.validateLicense(txtLicense.getText()));
        msg.add(Validation.validateUniqueID(txtUniqueNumber.getText()));
        msg.add(Validation.validateBankAc(txtBankno.getText()));
        msg.add(Validation.validateLinkedIn(txtLinkedin.getText()));
        msg.add(Validation.validatePhoneNumber(txtphoneNumber.getText()));
        msg.add(Validation.validateFaxNumber(txtFax.getText()));
        msg.add(Validation.validateEmailAddress(txtEmail.getText()));
        msg.add(Validation.validateMedicalRN(txtMedicalno.getText()));
        msg.add(Validation.validateHealthBN(txtHealthno.getText()));
        msg.add(Validation.validateVehicleIN(txtVehicleIdentificationNumber.getText()));
        msg.add(Validation.validateVehicleLP(txtVehicleLicensePlate.getText()));
        msg.add(Validation.validateDeviceId(txtDeviceID.getText()));
        msg.add(Validation.validateDeviceSN(txtDeviceSerialNumber.getText()));
        msg.add(Validation.validateIp(txtIP.getText()));
        msg.add(Validation.validateZipCode(txtZip.getText()));
        
        List diagMsg=new ArrayList();
        for(Object d:msg){
            if(d!=null){
                diagMsg.add(d);
            }
        }
        return diagMsg;
    }
