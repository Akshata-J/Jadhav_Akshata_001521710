            profile.setMedicalRecordNumber(Long.parseLong(txtMedicalno.getText()));

            profile.setHealthBeneficiaryNumber(txtHealthno.getText());

            profile.setBankAccountNumber(Long.parseLong(txtBankno.getText()));

            profile.setBirthCertificateNumber(txtBirthCertificate.getText());

            profile.setLicense_no(txtVehicleIdentificationNumber.getText());

            profile.setVehicleIdentificationNumber(txtVehicleIdentificationNumber.getText());

            profile.setVehicleLicensePlate(txtVehicleLicensePlate.getText());

            profile.setDeviceId(txtDeviceID.getText());

            profile.setDeviceSerialNumber(Long.parseLong(txtDeviceSerialNumber.getText()));

            profile.setLinkedin(txtLinkedin.getText());

            profile.setIp(txtIP.getText());

            profile.setBiometricFingerPrint(profileFingerprints);
            profile.setPhoto(profilePhoto);
            profile.setUniqueIdentifier(txtUniqueNumber.getText());
            profilePhoto=null;
            profileFingerprints=null;

            JOptionPane.showMessageDialog(this, "Profile is created.","Info",JOptionPane.INFORMATION_MESSAGE);

          
